package com.slangme.model;

public class SlangTerm {
	private int slangId;
	private String slangTerm;
	private String slangDescription;
	private String slangAudio;
	/**
	 * @return the slangId
	 */
	public int getSlangId() {
		return slangId;
	}
	/**
	 * @param slangId the slangId to set
	 */
	public void setSlangId(int slangId) {
		this.slangId = slangId;
	}
	/**
	 * @return the slangTerm
	 */
	public String getSlangTerm() {
		return slangTerm;
	}
	/**
	 * @param slangTerm the slangTerm to set
	 */
	public void setSlangTerm(String slangTerm) {
		this.slangTerm = slangTerm;
	}
	/**
	 * @return the slangDescription
	 */
	public String getSlangDescription() {
		return slangDescription;
	}
	/**
	 * @param slangDescription the slangDescription to set
	 */
	public void setSlangDescription(String slangDescription) {
		this.slangDescription = slangDescription;
	}
	/**
	 * @return the slangAudio
	 */
	public String getSlangAudio() {
		return slangAudio;
	}
	/**
	 * @param slangAudio the slangAudio to set
	 */
	public void setSlangAudio(String slangAudio) {
		this.slangAudio = slangAudio;
	}
}
